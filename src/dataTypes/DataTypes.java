package dataTypes;

public class DataTypes {

    public static void types(){



        //very small numbers
        byte verySmall = 100;
        short alsoSmall = 32000;

        //commonly used number variable in java
        int commonUsed = 50000;

        //need "L" character in end
        long veryLarge = 10000000000L;

        //decimals
        float notPreciseEnough = 3.14f;
        double morePrecise = 3.14588756d;

        //Boolean is also primitive data type
        boolean trueOrFalse = true;

        //char is also primitive, must be single quotes, not double!
        char character = 'A';

        System.out.println("character is :" + character);
    }
}
